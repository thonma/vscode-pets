import { PetColor } from '../../common/types';
import { BasePetType } from '../basepettype';
import { States, resolveState } from '../states';

export class Dog extends BasePetType {
    label = 'dog';
    static possibleColors = [
        PetColor.pluto,
        PetColor.black,
        PetColor.brown,
        PetColor.white,
        PetColor.red,
        PetColor.akita,
    ];
    sequence = {
        startingState: States.sitIdle,
        sequenceStates: [
            {
                state: States.sitIdle,
                possibleNextStates: [
                    States.walkRight,
                    States.runRight,
                    States.lie,
                ],
            },
            {
                state: States.lie,
                possibleNextStates: [States.walkRight, States.runRight],
            },
            {
                state: States.walkRight,
                possibleNextStates: [States.lie, States.walkLeft, States.runLeft],
            },
            {
                state: States.runRight,
                possibleNextStates: [States.lie, States.walkLeft, States.runLeft],
            },
            {
                state: States.walkLeft,
                possibleNextStates: [
                    States.sitIdle,
                    States.lie,
                    States.walkRight,
                    States.runRight,
                ],
            },
            {
                state: States.runLeft,
                possibleNextStates: [
                    States.sitIdle,
                    States.lie,
                    States.walkRight,
                    States.runRight,
                ],
            },
            {
                state: States.chase,
                possibleNextStates: [States.idleWithBall],
            },
            {
                state: States.idleWithBall,
                possibleNextStates: [
                    States.lie,
                    States.walkRight,
                    States.walkLeft,
                    States.runLeft,
                    States.runRight,
                ],
            },
        ],
    };
    get emoji(): string {
        return 'üê∂';
    }
    get hello(): string {
        return ` Every dog has its day - and today is woof day! Today I just want to bark. Take me on a walk`;
    }
    swipe() {
        if (this.currentStateEnum === States.swipe) {
            return;
        }
        this.holdState = this.currentState;
        this.holdStateEnum = this.currentStateEnum;
        this.currentStateEnum = States.swipe;
        this.currentState = resolveState(this.currentStateEnum, this);
        const reactions = [
            'üíï',
            'üçï',
            'üçñ',
            'üç∫',
            'üëã',
        ];
        this.showSpeechBubble(reactions[Math.floor(Math.random() * reactions.length)]);
    }
}

